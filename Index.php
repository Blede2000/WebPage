          <h1>Acusaciones malintencionadas buscan afectar dignidad de la institución': UNAD</h1>
          <p>La Universidad Nacional Abierta y a Distancia (UNAD) desestimó los señalamientos del Ministerio de Educación sobre presuntas irregularidades administrativas y académicas.</p>
          </br>
          <p>A través de un comunicado de prensa, la UNAD declaró que las acusaciones sobre malos manejos administrativos “buscan deslegitimar la misión y el compromiso” que tiene la institución con la educación del país.</p>
          </br>
          <p>“Los rumores que circulan sobre el particular son totalmente infundados y han sido promovidos por personas identificadas, que de manera malintencionada, buscan afectar la dignidad de (la comunidad educativa), y la bien ganada reputación institucional durante sus 33 años de existencia. Todo ello gestado en el contexto que se adelanta durante el actual proceso de designación rectoral”, indicó la UNAD</p>
          </br>
          <p>Reiterando que no ha sido objeto de ninguna sanción por parte del Ministerio de Educación, ni de ningún organismo de control (Procuraduría, Fiscalía o Contraloría), la Universidad declaró que “goza de plena estabilidad académica y administrativa”.

“La UNAD responde de manera transparente por su sanidad y sostenibilidad financiera; razón por la cual cumple a cabalidad y oportunamente con la totalidad de sus obligaciones laborales y contractuales con el personal académico y administrativo”, agregó la institución.</p>
