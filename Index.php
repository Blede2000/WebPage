  			<h1>UNAD es cuestionada por abrir sede en EE.UU</h1>
        <p>En 2001, la Universidad Nacional Abierta y a Distancia, una institución pública, decidió abrir una sede en Estados Unidos con el objetivo inicial de ofrecer programas académicos a los más de 8.000 presos colombianos confinados en las cárceles de ese país.</p>
        </br>
        <p>Ahora, más de una década después de haber iniciado su operación en el exterior, las directivas de la institución enfrentan duros señalamientos por parte de la Contraloría General de la República.</p>
        </br>
        <p>En un comunicado público, el ente de control cuestionó la actuación de las directivas y la inversión por $6.230 millones que realizaron para crear una sede en Florida (Estados Unidos) “de naturaleza privada y regida por las leyes y normas de ese país”.

Según la Contraloría, tanto la junta directiva como el rector de la UNAD traspasaron el marco de las limitaciones establecidas. “Teniendo en cuenta que la sede en Florida es una corporación sin ánimo de lucro registrada y domiciliada en los Estados Unidos de Norte América, se evidenció un detrimento por $5.939,93 millones, por desembolsar recursos de la Nación para la creación y el mantenimiento de un tercero particular”, señaló el ente de control.</p>
        <img src="http://www.elespectador.com/files/imagecache/560_width_display/img_ipad/0d5a95a74b994821d0f2046db9ee7c85.jpg" class="responsive">
        <p>Además de esta inversión, la Contraloría cuestionó el pago con recursos públicos de viáticos y comisiones a Florida para el rector y otros funcionarios, como también a empleados de esa sede, constituyendo un detrimento patrimonial en cuantía superior a los $290 millones.</p>
        </br>
        <p>La asesora jurídica de la UNAD, Ángela Melo, le explicó a El Espectador que la sede de la universidad en Florida se abrió por petición de la Cancillería, con el propósito inicial de atender a la población colombiana en las cárceles de Estados Unidos, y más adelante incluyó a la población latina en la región.</p>
